package com.project.skiersendpoint;

public class SkierRequest {
	    private String time;
	    private String liftID;

	    // Getters and setters
	    public String getTime() {
	        return time;
	    }

	    public void setTime(String time) {
	        this.time = time;
	    }

	    public String getLiftID() {
	        return liftID;
	    }

	    public void setLiftId(String liftID) {
	        this.liftID = liftID;
	    }
	}
